# -*- coding: utf-8 -*-


class Color:
    """ ANSI Color Code """
    WHITE = '\033[1;37m'
    BLACK = '\033[0;30m'
    RED = '\033[0;31m'
    YELLOW = '\033[1;33m'
    GRAY = '\033[0;37m'
    BLUE = '\033[94m'
    GREEN = '\033[92m'
    WARNING = '\033[93m'

    HEADER = '\033[95m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
